def patch_branding(base) -> None:
    old_page = base.page
    old_home = base.home

    def page(paper: dict, preview_name: str) -> str:
        text = old_page(paper, preview_name)
        text = text.replace("Annotated Paper2Web", "Paper Reading HTML")
        text = text.replace("paper-2-web · 批注模式", "paper-reading-html · 精读模式")
        return text

    def home(cards: str) -> str:
        text = old_home(cards)
        text = text.replace("paper-2-web · 批注模式", "paper-reading-html · 精读模式")
        text = text.replace("repro / paper2web", "repro / paper-reading-html")
        text = text.replace("Prompt A / Prompt B 的网页化落地", "paper-reading-html 的精读网页预览")
        text = text.replace("三篇论文的双板块 HTML 样例", "三篇论文的精读网页样例")
        return text

    base.page = page
    base.home = home


