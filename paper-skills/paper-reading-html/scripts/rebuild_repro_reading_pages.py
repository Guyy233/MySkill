def load_base():
    spec = importlib.util.spec_from_file_location("repro_base", BASE_SCRIPT)
    module = importlib.util.module_from_spec(spec)
    assert spec.loader is not None
    spec.loader.exec_module(module)
    return module


def get_paper(papers: list[dict], slug: str) -> dict:
    for paper in papers:
        if paper["slug"] == slug:
            return paper
    raise KeyError(slug)


def get_chapter(paper: dict, chapter_id: str) -> dict:
    for chapter in paper["chapters"]:
        if chapter["id"] == chapter_id:
            return chapter
    raise KeyError(chapter_id)


def append_row(paper: dict, chapter_id: str, row: dict) -> None:
    get_chapter(paper, chapter_id)["rows"].append(row)


